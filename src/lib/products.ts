export function getProductBySlug(slug: string) {
  return products.find((p) => p.slug === slug)
}

export function getBestSellers() {
  return products.filter((p) => p.bestSeller)
}

export function getRelatedProducts(product: Product, count = 4) {
  return products.filter((p) => p.category === product.category && p.id !== product.id).slice(0, count)
}

export function getLocalizedProduct(product: Product, locale: 'fr' | 'ar'): Product {
  if (locale !== 'ar') return product
  return {
    ...product,
    name: product.nameAr ?? product.name,
    tagline: product.taglineAr ?? product.tagline,
    description: product.descriptionAr ?? product.description,
    ingredients: product.ingredientsAr ?? product.ingredients,
    howToUse: product.howToUseAr ?? product.howToUse,
  }
}
