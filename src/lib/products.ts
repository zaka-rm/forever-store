export type StockStatus = 'untracked' | 'out' | 'low' | 'in'

export function stockStatus(stock?: number | null): StockStatus {
  if (stock === null || stock === undefined) return 'untracked'
  if (stock <= 0) return 'out'
  if (stock <= 5) return 'low'
  return 'in'
}

