// Generates supabase/03_products-seed.sql from the bundled catalogue in
// src/lib/products.ts, so the database starts with all existing products.
// Run once: node scripts/gen-products-seed.cjs
// Then paste the generated SQL into Supabase → SQL Editor and Run.

const fs = require('fs')
const path = require('path')
const esbuild = require('esbuild')

const srcPath = path.join(__dirname, '../src/lib/products.ts')
const tmpPath = path.join(__dirname, '_products_tmp.cjs')

// Transpile the TS source to a temporary CommonJS file we can require().
esbuild.buildSync({
  entryPoints: [srcPath],
  bundle: true,
  format: 'cjs',
  platform: 'node',
  outfile: tmpPath,
})

const { products } = require(tmpPath)
fs.unlinkSync(tmpPath)

function q(value) {
  if (value === undefined || value === null) return 'NULL'
  return `'${String(value).replace(/'/g, "''")}'`
}

function jsonb(value) {
  if (value === undefined || value === null) return 'NULL'
  return `'${JSON.stringify(value).replace(/'/g, "''")}'::jsonb`
}

function bool(value) {
  return value ? 'true' : 'false'
}

const columns = [
  'id', 'slug', 'name', 'name_ar', 'category', 'price', 'rating', 'review_count',
  'tagline', 'tagline_ar', 'description', 'description_ar', 'ingredients',
  'ingredients_ar', 'how_to_use', 'how_to_use_ar', 'image', 'size',
  'best_seller', 'is_new', 'pack_contents', 'sort_order',
]

const rows = products.map((p, i) => {
  const values = [
    q(p.id), q(p.slug), q(p.name), q(p.nameAr), q(p.category),
    Number(p.price), Number(p.rating), Number(p.reviewCount ?? 0),
    q(p.tagline), q(p.taglineAr), q(p.description), q(p.descriptionAr),
    jsonb(p.ingredients ?? []), jsonb(p.ingredientsAr),
    q(p.howToUse), q(p.howToUseAr), q(p.image), q(p.size),
    bool(p.bestSeller), bool(p.new), jsonb(p.packContents), i,
  ]
  return `  (${values.join(', ')})`
})

const sql =
  `-- Auto-generated by scripts/gen-products-seed.cjs — do not edit by hand.\n` +
  `-- Run in Supabase → SQL Editor after 02_products-and-admin.sql.\n\n` +
  `insert into products (${columns.join(', ')}) values\n` +
  `${rows.join(',\n')}\n` +
  `on conflict (id) do nothing;\n`

const outPath = path.join(__dirname, '../supabase/03_products-seed.sql')
fs.writeFileSync(outPath, sql)
console.log(`Wrote ${outPath} with ${products.length} products.`)
